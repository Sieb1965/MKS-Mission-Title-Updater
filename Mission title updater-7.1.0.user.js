// Missienamen-module (v7)
// Vervangt missie-captions door eigen namen (uit `missionNames`) in:
//   • de sidebar-missielijst
//   • de missiepagina (/missions/<id>)
//   • tooltips/labels op de Leaflet-kaart (incl. gedeelde meldingen)
//
// Versiegeschiedenis (samengevat):
//   v2  Tooltip-update bij 'tooltipopen' i.p.v. eenmalig 'layeradd';
//       operator-precedence bug en naamconflict (missionIds/missionNames)
//       gefixt; guards tegen ontbrekende elementen; sidebar-observer met
//       subtree zodat missie-uitbreidingen worden opgepikt.
//   v3  Naam-cache per missie-id (tooltips werken ook als de sidebar-entry
//       tijdelijk weg is); TreeWalker-fallback voor geneste captions.
//   v4  Sidebar-observer aan één stabiele root (overleeft herbouwen van
//       lijsten, o.a. bij delen); characterData-mutaties worden opgepikt;
//       replace-functies idempotent (geen mutatie-loops); updates
//       gedebounced via requestAnimationFrame.
//   v5  Pane-observer: zichtbare kaartlabels worden direct in de DOM
//       herschreven, onafhankelijk van tooltipopen/layeradd-events.
//   v6  Gedeelde meldingen: hun tooltips bevatten alleen de kale caption
//       (géén mission_address_-element). Reverse-map (originele caption →
//       nieuwe naam) als fallback-lookup.
//   v7  Opschoning: changelog samengevat, null-guard in missionUpdate,
//       consistente stijl, comments bij bewuste redundantie.
//   v7.1 Fix: kaartlabels van meldingen met patiënten/arrestanten sprongen
//        terug naar de speltitel zodra een voertuig aanrijdend ging. LSSM's
//        Extended Call List plakt daar een teller achter de caption, waardoor
//        de lookup op de volledige tekst nooit meer matchte. De caption-only
//        route kijkt nu alleen naar de caption-textnode en vervangt ook
//        alleen die node, zodat de tellers behouden blijven.
// ═══════════════════════════════════════════════════════════════════════════

(function () {
    'use strict';

    const NAMES = (typeof missionNames !== 'undefined' && missionNames)
        ? missionNames
        : new Proxy({}, { get: (_t, prop) => (typeof prop === 'string' ? prop : undefined) });

    // missionId (string) → naam. Gevuld zodra we een sidebar-entry zien,
    // zodat tooltips niet afhankelijk zijn van de sidebar-DOM op hover-moment.
    const nameCache = new Map();

    // Genormaliseerde ORIGINELE caption → nieuwe naam. Tooltips van gedeelde
    // missies bevatten alleen de kale caption-tekst; via deze map kunnen we
    // die alsnog vervangen, ook zonder missie-id.
    const captionNameCache = new Map();

    function normalizeCaption(text) {
        return String(text).replace(/[\s,]+$/, '').trim();
    }

    // Tooltip/label zónder mission_address_-element: in de basis alleen de kale
    // caption, maar andere scripts kunnen daar iets achter plakken. LSSM v4's
    // Extended Call List voegt bij meldingen met patiënten of arrestanten
    // comment-markers + een icoon + aantal toe (<!--lssmv4-ecl-...-->&nbsp;<i>…</i> 1).
    // De caption zelf blijft dan de eerste niet-lege textnode. Daarom:
    //   1) alleen dié textnode gebruiken voor de cache-lookup (de hele
    //      textContent bevat ook het aantal en matcht dan nooit), en
    //   2) alleen dié textnode vervangen, zodat de tellers blijven staan.
    // Idempotent: na vervanging matcht de nieuwe naam geen originele caption
    // meer, dus geen mutatie-loop via de observers.
    function rewriteCaptionOnly(rootEl) {
        const captionNode = findCaptionTextNode(rootEl);
        if (!captionNode) return false;
        const newName = captionNameCache.get(normalizeCaption(captionNode.textContent));
        if (!newName) return false;
        if (captionNode.textContent !== newName) captionNode.textContent = newName;
        return true;
    }

    function findCaptionTextNode(parent) {
        return Array.from(parent.childNodes)
            .find(node => node.nodeType === Node.TEXT_NODE && node.textContent.trim()) || null;
    }

    function getMissionKey(missionTypeId, overlayIndex) {
        if (overlayIndex && overlayIndex !== 'null') {
            return `${missionTypeId}-${overlayIndex}`;
        }
        return missionTypeId;
    }

    // Vervangt de eerste niet-lege DIRECTE textnode van parent.
    // Idempotent: als de tekst al klopt, wordt niets gemuteerd — essentieel
    // omdat observers met characterData anders in een loop raken.
    function replaceCaptionTextNode(parent, newText) {
        const textNode = findCaptionTextNode(parent);
        if (!textNode) return false;
        if (textNode.textContent === newText) return true; // al goed → geen loop
        textNode.textContent = newText;
        return true;
    }

    // Fallback: zoekt de eerste niet-lege textnode wáár dan ook in de boom,
    // maar slaat nodes binnen `skipEl` over (bijv. het adres-element).
    // Idempotent, om dezelfde reden als hierboven.
    function replaceCaptionTextNodeDeep(root, newText, skipEl) {
        const walker = document.createTreeWalker(root, NodeFilter.SHOW_TEXT, {
            acceptNode(node) {
                if (!node.textContent.trim()) return NodeFilter.FILTER_REJECT;
                if (skipEl && skipEl.contains(node)) return NodeFilter.FILTER_REJECT;
                return NodeFilter.FILTER_ACCEPT;
            }
        });
        const textNode = walker.nextNode();
        if (!textNode) return false;
        if (textNode.textContent === newText) return true; // al goed → geen loop
        textNode.textContent = newText;
        return true;
    }

    function getNameForMissionElement(el) {
        if (!el) return null;
        const missionTypeId = el.getAttribute('mission_type_id');
        if (!missionTypeId) return null;
        const overlayIndex = el.getAttribute('data-overlay-index');
        return NAMES[getMissionKey(missionTypeId, overlayIndex)] || null;
    }

    // Centrale lookup mét cache. Probeert eerst de live sidebar-entry
    // (die is het meest actueel, o.a. bij missie-uitbreidingen) en valt
    // daarna terug op de cache.
    function getNameForMissionId(missionId) {
        const sidebarEntry = document.getElementById(`mission_${missionId}`);
        const liveName = getNameForMissionElement(sidebarEntry);
        if (liveName) {
            nameCache.set(String(missionId), liveName);
            return liveName;
        }
        return nameCache.get(String(missionId)) || null;
    }

    // ── Missiepagina (/missions/<id>) ────────────────────────────────────────
    function missionUpdate() {
        const missionInfo = document.getElementById('mission_general_info');
        const title = document.getElementById('missionH1');
        if (!missionInfo || !title) return;

        const missionTypeId = missionInfo.getAttribute('data-mission-type');
        if (!missionTypeId) return; // guard: voorkomt NAMES[null] → "null"-titel

        const missionKey = getMissionKey(
            missionTypeId,
            missionInfo.getAttribute('data-overlay-index')
        );

        if (NAMES[missionKey]) {
            replaceCaptionTextNode(title, ' ' + NAMES[missionKey]);
        }

        const oldCaption = title.getElementsByTagName('s');
        if (oldCaption[0]) oldCaption[0].remove();
    }

    // ── Sidebar ──────────────────────────────────────────────────────────────
    function sidebarMissionUpdate() {
        const sidebarEntries = document.getElementsByClassName('missionSideBarEntry');
        for (const sidebarEntry of sidebarEntries) {
            const name = getNameForMissionElement(sidebarEntry);
            if (!name) continue;

            const missionId = sidebarEntry.getAttribute('mission_id');
            if (missionId) nameCache.set(String(missionId), name); // ook cachen als caption-update faalt

            const title = document.getElementById(`mission_caption_${missionId}`);
            if (!title) continue;

            // Originele caption vastleggen vóór we hem overschrijven, voor de
            // fallback-lookup bij gedeelde meldingen. Alleen als het nog de
            // speltekst is (niet al onze eigen naam).
            const captionNode = findCaptionTextNode(title);
            if (captionNode) {
                const original = normalizeCaption(captionNode.textContent);
                if (original && original !== normalizeCaption(name)) {
                    captionNameCache.set(original, name);
                }
            }

            replaceCaptionTextNode(title, name + ', ');

            const oldCaption = document.getElementById(`mission_old_caption_${missionId}`);
            if (oldCaption) oldCaption.remove();
        }
    }

    // Debounce via requestAnimationFrame: meerdere mutaties in één frame
    // (bijv. het herbouwen van een hele lijst na delen) → één update.
    let sidebarUpdateScheduled = false;
    function scheduleSidebarUpdate() {
        if (sidebarUpdateScheduled) return;
        sidebarUpdateScheduled = true;
        requestAnimationFrame(() => {
            sidebarUpdateScheduled = false;
            sidebarMissionUpdate();
        });
    }

    function setupSidebarObserver() {
        sidebarMissionUpdate();

        // Eén stabiele root i.p.v. de losse mission_list-elementen: overleeft
        // het herbouwen/vervangen van lijsten bij o.a. het delen van een
        // melding. characterData vangt pure tekst-terugzettingen.
        const root = document.getElementById('missions-panel-body')
            || document.getElementById('missions_outer')
            || document.body;

        new MutationObserver(scheduleSidebarUpdate).observe(root, {
            childList: true,
            subtree: true,
            characterData: true
        });
    }

    // ── Tooltips op de kaart ─────────────────────────────────────────────────
    function setupTooltipHandling(attempt = 0) {
        const W = (typeof unsafeWindow !== 'undefined') ? unsafeWindow : window;
        const map = W.map;

        if (!map || typeof map.on !== 'function') {
            if (attempt < 20) {
                setTimeout(() => setupTooltipHandling(attempt + 1), 500);
            } else {
                console.warn('[MKS] Leaflet map niet gevonden; tooltip-namen uitgeschakeld.');
            }
            return;
        }

        const scratch = document.createElement('div');

        // Route 1: via het Leaflet-tooltip-object (setContent). Houdt de
        // interne content-string in sync, zodat een tooltip bij (her)openen
        // niet één frame de oude tekst toont.
        function rewriteTooltip(tooltip) {
            if (!tooltip || typeof tooltip.getContent !== 'function') return;
            const original = tooltip.getContent();
            if (typeof original !== 'string' || !original) return;

            scratch.innerHTML = original;

            const addressEl = scratch.querySelector('[id^="mission_address_"]');
            if (!addressEl) {
                // Gedeelde missie of caption-only label (evt. met ECL-tellers).
                if (rewriteCaptionOnly(scratch) && scratch.innerHTML !== original) {
                    tooltip.setContent(scratch.innerHTML);
                }
                return;
            }

            const missionId = addressEl.id.split('_').pop();
            const name = getNameForMissionId(missionId);
            if (!name) return;

            scratch.querySelectorAll('s').forEach(s => s.remove());

            // Eerst directe textnode, anders dieper zoeken (caption in <b>/<span>).
            const replaced =
                replaceCaptionTextNode(scratch, name + ', ') ||
                replaceCaptionTextNodeDeep(scratch, name + ', ', addressEl);
            if (!replaced) return;

            if (scratch.innerHTML !== original) tooltip.setContent(scratch.innerHTML);
        }

        function rewriteLayerTooltip(layer) {
            if (layer && typeof layer.getTooltip === 'function') {
                const t = layer.getTooltip();
                if (t) rewriteTooltip(t);
            }
        }

        // BEWUSTE REDUNDANTIE: deze events overlappen deels met de
        // pane-observer hieronder. Toch laten staan — de pane-observer fixt
        // alleen de zichtbare DOM, terwijl deze route Leaflets interne
        // content-string bijwerkt. Zonder deze route flitst bij het heropenen
        // van een tooltip één frame de originele tekst.
        map.on('tooltipopen', event => rewriteTooltip(event.tooltip));
        map.on('layeradd', event => rewriteLayerTooltip(event.layer));
        map.eachLayer(rewriteLayerTooltip);

        // Route 2: pane-observer. Het spel zet tooltip-content soms direct
        // terug via setContent() (o.a. na het delen van een melding); daar
        // vuurt geen tooltipopen/layeradd voor. Daarom herschrijven we de
        // zichtbare tooltip-DOM zelf zodra die muteert.
        function rewriteTooltipDom(tooltipEl) {
            const addressEl = tooltipEl.querySelector('[id^="mission_address_"]');
            if (!addressEl) {
                // Gedeelde missie of caption-only label (evt. met ECL-tellers).
                // NB: níet tooltipEl.textContent overschrijven — dat zou de
                // patiënten-/arrestantenteller van LSSM wissen.
                rewriteCaptionOnly(tooltipEl);
                return;
            }

            const missionId = addressEl.id.split('_').pop();
            const name = getNameForMissionId(missionId);
            if (!name) return;

            tooltipEl.querySelectorAll('s').forEach(s => s.remove());

            // Zelfde patroon als rewriteTooltip: eerst direct, anders diep.
            void (replaceCaptionTextNode(tooltipEl, name + ', ') ||
                replaceCaptionTextNodeDeep(tooltipEl, name + ', ', addressEl));
        }

        function rewriteAllVisibleTooltips(pane) {
            pane.querySelectorAll('.leaflet-tooltip').forEach(rewriteTooltipDom);
        }

        const pane = (typeof map.getPane === 'function' && map.getPane('tooltipPane'))
            || document.querySelector('.leaflet-tooltip-pane');

        if (pane) {
            let paneUpdateScheduled = false;
            new MutationObserver(() => {
                if (paneUpdateScheduled) return;
                paneUpdateScheduled = true;
                requestAnimationFrame(() => {
                    paneUpdateScheduled = false;
                    rewriteAllVisibleTooltips(pane);
                });
            }).observe(pane, {
                childList: true,
                subtree: true,
                characterData: true
            });

            rewriteAllVisibleTooltips(pane); // ook wat er nu al staat
        } else {
            console.warn('[MKS] Tooltip-pane niet gevonden; kaartlabel-fallback uitgeschakeld.');
        }
    }

    // ── Init ─────────────────────────────────────────────────────────────────
    if (location.pathname.startsWith('/missions/')) missionUpdate();

    if (location.pathname !== '/') return;
    setupSidebarObserver(); // eerst: vult meteen nameCache én captionNameCache
    setupTooltipHandling();
})();
